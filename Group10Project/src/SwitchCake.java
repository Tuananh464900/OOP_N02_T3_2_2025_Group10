public class SwitchCake {
    public static void SwitchCase(){
        int type = 2;

        switch (type) {
            case 1:

            System.out.println("Banh chocolate");
            break;
            case 2:

            System.out.println("Banh vani");
            break;
            default:

            System.out.println("Banh khac");
        }
    }
}